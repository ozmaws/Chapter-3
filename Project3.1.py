fstSide = input("Length of the first side of the triangle: ")
scndSide = input("Length of the second side of the triangle: ")
thrdSide = input("Length of the third side of the triangle: ")
if fstSide == scndSide == thrdSide:
  print("This is an equilateral triangle.")
else:
  print("This is not an equilateral triangle.")
